     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(MediaRequest $mediaRequest)
    {
        $image = $mediaRequest->image;
        $imageName = $image->getClientOriginalName();
        if( file_exists('uploads/' . $imageName) ) {
            return back()->with('image-error', "Ce nom d'image existe déjà");
        } else {
            $image->move('uploads/', $imageName);
            $media = Media::create([
                "image" => $imageName,
            ]);
            return back()->with('info', 'L\'image a bien été insérée');
        }
    }

